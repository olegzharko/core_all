#include "libft.h"

int	ft_strcmp(const char *s1, const char *s2)
{
	size_t	len_s1;
	size_t	len_s2;

	len_s1 = ft_strlen(s1);
	len_s2 = ft_strlen(s2);
	return (ft_memcmp(s1, s2, (len_s1 > len_s2) ? len_s1 : len_s2));
}
